my $apostropheHtml = '&#39;';
sub translate
{
    (my $phrasesHashRef, my $lineRef) = @_;
    for my $eng (keys %$phrasesHashRef)
    {
        my $rus = $phrasesHashRef->{$eng};
        $$lineRef =~ s/$eng/$rus/g;
        # apostrophe may be written using HTML code
        $$lineRef =~ s/$eng/$rus/g if $eng =~ s/'/$apostropheHtml/g
    }
}

